    {
        $rules = [
            'country_id' => 'required|numeric'
        ];
        $validator = Validator::make(request()->all(), $rules);
        if($validator->fails()){
            result( 401, $validator->messages(), null);
        }
        else{
            $cities = City::where('country_id',request('country_id'))->get();
            result(200,'Success Request',$cities);
        }
    }

